package com.ck.mcs;

public class Answer {
    private int AnswerID;
    private String AnswerFrom;
    private int AnswerTo;
    private String AnswerCreateTime;
    private String AnswerText;
    public int getAnswerID() {
        return AnswerID;
    }
    public void setAnswerID(int answerID) {
        AnswerID = answerID;
    }
    public String getAnswerFrom() {
        return AnswerFrom;
    }
    public void setAnswerFrom(String answerFrom) {
        AnswerFrom = answerFrom;
    }
    public int getAnswerTo() {
        return AnswerTo;
    }
    public void setAnswerTo(int answerTo) {
        AnswerTo = answerTo;
    }
    public String getAnswerCreateTime() {
        return AnswerCreateTime;
    }
    public void setAnswerCreateTime(String answerCreateTime) {
        AnswerCreateTime = answerCreateTime;
    }
    public String getAnswerText() {
        return AnswerText;
    }
    public void setAnswerText(String answerText) {
        AnswerText = answerText;
    }
}
